#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*csrc;
	char	*cdst;
	size_t	i;

	csrc = (char *)src;
	cdst = (char *)dst;
	i = 0;
	while (dst == src || !n)
	{
		return (dst);
	}
	while (i < n)
	{
		cdst[i] = csrc[i];
		i++;
	}
	return (dst);
}
